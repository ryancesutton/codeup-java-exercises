public class StringExercise {

    public static void main(String[] args) {

        String brickInWall = "We don't need no education \nWe don't need no thought control";
        System.out.println(brickInWall);

        String checkItOut = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(checkItOut);

        String inWindows = "In windows, the main drive is usually C:\\";
        System.out.println(inWindows);

        String backSlash = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(backSlash);

    }
}
